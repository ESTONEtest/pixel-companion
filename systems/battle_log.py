class BattleLogSystem:

    # ==================================================
    # ITEM NAMES
    # ==================================================

    ITEM_NAMES = {
        "wood": "🪵 Дерево",
        "iron": "⛓️ Железо",
        "crystal": "💎 Кристалл",
        "potion": "🧪 Зелье"
    }

    # ==================================================
    # FORMAT LOOT
    # ==================================================

    def format_loot(
        self,
        loot: str
    ):

        return self.ITEM_NAMES.get(
            loot,
            loot
        )

    # ==================================================
    # START
    # ==================================================

    def start(
        self,
        monster_name: str
    ):

        return f"⚔️ Бой начался! Противник: **{monster_name}**."

    # ==================================================
    # PLAYER ATTACK
    # ==================================================

    def player_attack(
        self,
        damage: int
    ):

        return f"⚔️ Вы нанесли монстру **{damage}** урона."

    # ==================================================
    # PLAYER CRITICAL
    # ==================================================

    def player_critical(
        self,
        damage: int
    ):

        return f"💥 **Критический удар!** Вы нанесли **{damage}** урона."

    # ==================================================
    # MONSTER ATTACK
    # ==================================================

    def monster_attack(
        self,
        monster_name: str,
        damage: int
    ):

        return f"👹 **{monster_name}** атакует и наносит вам **{damage}** урона."

    # ==================================================
    # HEAL
    # ==================================================

    def heal(
        self,
        healed: int
    ):

        return f"🧪 Вы восстановили **{healed} HP**."

    # ==================================================
    # DEFEND
    # ==================================================

    def defend(self):

        return "🛡️ Вы приготовились к защите. Ваша защита временно увеличена."

    # ==================================================
    # RUN
    # ==================================================

    def run(self):

        return "🏃 Вы сбежали с поля боя. Бой завершен."

    # ==================================================
    # DEFEAT
    # ==================================================

    def defeat(self):

        return "💀 Бой завершен поражением. Монстр остался жив."

    # ==================================================
    # VICTORY
    # ==================================================

    def victory(
        self,
        monster_name: str
    ):

        return f"🏆 Победа! Вы одолели **{monster_name}**."

    # ==================================================
    # REWARD XP
    # ==================================================

    def reward_xp(
        self,
        xp: int
    ):

        return f"✨ Получено опыта: **{xp}**."

    # ==================================================
    # REWARD COINS
    # ==================================================

    def reward_coins(
        self,
        coins: int
    ):

        return f"🪙 Получено монет: **{coins}**."

    # ==================================================
    # LOOT
    # ==================================================

    def loot(
        self,
        loot: str | None
    ):

        if loot:

            return f"🎁 Добыча: **{self.format_loot(loot)}**."

        return "🎁 Добыча не выпала."


battle_log_system = BattleLogSystem()